#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	n;
	int	d;

	i = 0;
	n = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\r'
			|| str[i] == '\v' || str[i] == '\f')
		i++;
	d = i;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
		n = n * 10 + str[i++] - '0';
	if (str[d] == '-')
		n = -n;
	return (n);
}
