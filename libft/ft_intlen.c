#include "libft.h"

int	ft_intlen(int nb)
{
	int	i;

	if (nb == 0)
		return (1);
	i = nb < 0 ? 2 : 1;
	while ((nb >= 10 || nb <= -10) && i++)
		nb /= 10;
	return (i);
}
