#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int	i;
	int	d;

	i = 0;
	while (dest[i] != '\0')
		i++;
	d = -1;
	while (src[++d] != '\0')
		dest[i + d] = src[d];
	dest[i + d] = '\0';
	return (dest);
}
