static int	ft_bit8(int n)
{
	int r;

	r = BIT_8;
	while (n)
	{
		r /= 2;
		n--;
	}
	return (r);
}

void		constantes_des_permutation_p(t_des *des)
{
	int	i;
	int	perm[32] = {15,  6, 19, 20, 28, 11, 27, 16,  0, 14, 22, 25, 4, 17, 30,  9,
					 1,  7, 23, 13, 31, 26,  2,  8, 18, 12, 29,  5, 21, 10, 3, 24};

	// printf("Enter to %s\n", __FUNCTION__);
	i = -1;
	while (++i < 32)
		if (des->box[perm[i] / 8] & ft_bit8(perm[i] % 8))
			des->perm[i / 8] |= 1 << (7 - (i % 8));
}

void		constantes_des_permuted_choice_2(t_des *des, int id)
{
	int	i;
	int	perm[48] = {13, 16, 10, 23,  0,  4,  2, 27, 14,  5, 20,  9,
					22, 18, 11,  3, 25,  7, 15,  6, 26, 19, 12,  1,
					40, 51, 30, 36, 46, 54, 29, 39, 50, 44, 32, 47,
					43, 48, 38, 55, 33, 52, 45, 41, 49, 35, 28, 31};

	// printf("Enter to %s\n", __FUNCTION__);
	i = -1;
	while (++i < 48)
	{
		if (perm[i] < 28)
		{
			if (des->leftkey[perm[i] / 7] & ft_bit(perm[i] % 7))
				des->subkeys[id][i / 6] |= 1 << (5 - (i % 6));
		}
		else if (des->rightkey[(perm[i] - 28) / 7] & ft_bit(perm[i] % 7))
			des->subkeys[id][i / 6] |= 1 << (5 - (i % 6));
	}
}
